public class Contestant {
    private String name;
    private String club;
    private int age;
    private long segment500Time;
    private long segment1000Time;
    private long segment1500Time;
    private long segment2000Time;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;

    }

    public String getClub() {
        return club;
    }

    public void setClub(String club) {
        this.club = club;

    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


    public long getSegment500Time() {
        return segment500Time;
    }

    public void setSegment500Time(long segment500Time) {
        this.segment500Time = segment500Time;
    }

    public long getSegment1000Time() {
        return segment1000Time;
    }

    public void setSegment1000Time(long segment1000Time) {
        this.segment1000Time = segment1000Time;
    }

    public long getSegment1500Time() {
        return segment1500Time;
    }

    public void setSegment1500Time(long segment1500Time) {
        this.segment1500Time = segment1500Time;
    }

    public long getSegment2000Time() {
        return segment2000Time;
    }

    public void setSegment2000Time(long segment2000Time) {
        this.segment2000Time = segment2000Time;
    }


}